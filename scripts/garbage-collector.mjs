#!/usr/bin/env node
/**
 * Orphan File Garbage Collector for Lotusa V2.
 * 
 * Scans the primary "uploads" storage bucket. If a file is older than 24 hours,
 * it queries the `projects` and `playlists` collections to see if any document
 * maintains an active reference to the fileId. If none are found, the file
 * is securely wiped from the cloud to conserve storage.
 * 
 * Run via Cron: `node scripts/garbage-collector.mjs`
 */

import "dotenv/config";
import { config } from "dotenv";
import { Client, Databases, Storage, Query } from "node-appwrite";

config({ path: ".env.local", override: true });

const ENDPOINT = process.env.NEXT_PUBLIC_APPWRITE_ENDPOINT;
const PROJECT_ID = process.env.NEXT_PUBLIC_APPWRITE_PROJECT_ID;
const API_KEY = process.env.APPWRITE_API_KEY;

const DATABASE_ID = process.env.NEXT_PUBLIC_APPWRITE_DATABASE_ID || "backing_score_db";
const PROJECTS_COLLECTION = process.env.NEXT_PUBLIC_APPWRITE_PROJECTS_COLLECTION_ID || "projects";
const PLAYLISTS_COLLECTION = "playlists";
const BUCKET_ID = process.env.NEXT_PUBLIC_APPWRITE_UPLOADS_BUCKET_ID || "uploads";

function requireEnv(name) {
  const v = process.env[name];
  if (!v || v.trim().length === 0) {
    console.error(`ERROR: Missing env var: ${name}. Please verify .env.local definitions.`);
    process.exit(1);
  }
}

async function main() {
  requireEnv("NEXT_PUBLIC_APPWRITE_ENDPOINT");
  requireEnv("NEXT_PUBLIC_APPWRITE_PROJECT_ID");
  requireEnv("APPWRITE_API_KEY");

  const client = new Client()
    .setEndpoint(ENDPOINT)
    .setProject(PROJECT_ID)
    .setKey(API_KEY);

  const storage = new Storage(client);
  const databases = new Databases(client);

  console.log(`[+] Initializing Orphan File Garbage Collection Sequence...`);
  console.log(`[+] Target Bucket: ${BUCKET_ID}`);
  
  const twentyFourHoursAgo = new Date(Date.now() - 24 * 60 * 60 * 1000).toISOString();
  console.log(`[+] Sweep Threshold Date: ${twentyFourHoursAgo} (Files uploaded strictly BEFORE this mark)`);

  let totalScanned = 0;
  let totalDeleted = 0;
  let hasMore = true;
  let cursor = null;

  try {
    while (hasMore) {
      const queries = [
        Query.limit(100),
        Query.orderDesc("$createdAt"), // Sweeping oldest first can technically be done via asc, but desc works logically
        Query.lessThan("$createdAt", twentyFourHoursAgo) // Crucial rule 1: >24HRs old
      ];
      if (cursor) queries.push(Query.cursorAfter(cursor));

      const response = await storage.listFiles(BUCKET_ID, queries);
      const files = response.files;
      
      if (files.length === 0) {
        hasMore = false;
        break;
      }

      for (const file of files) {
        totalScanned++;
        const fileId = file.$id;
        process.stdout.write(`\r[~] Scanning File ID ${fileId}...`);

        // Check if an existing Project claims this File ID in its payload JSON or coverUrl.
        // We do a simple exact string search relying on Database indexing algorithms.
        const projectRefs = await databases.listDocuments(DATABASE_ID, PROJECTS_COLLECTION, [
            // While we could parse `payload`, doing a blanket Search string matches JSON blobs implicitly!
            Query.search("payload", fileId),
            Query.limit(1)
        ]);
        
        // Also check if any Project is using it specifically as a Cover Image (coverUrl direct lookup)
        const coverRefs = await databases.listDocuments(DATABASE_ID, PROJECTS_COLLECTION, [
            Query.search("coverUrl", fileId),
            Query.limit(1)
        ]);

        // Check if a Playlist relies on this file ID
        const playlistRefs = await databases.listDocuments(DATABASE_ID, PLAYLISTS_COLLECTION, [
            Query.equal("coverImageId", fileId),
            Query.limit(1)
        ]);

        const hasAnyActiveReferences = (projectRefs.total > 0) || (coverRefs.total > 0) || (playlistRefs.total > 0);

        if (!hasAnyActiveReferences) {
          console.log(`\n[-] ORPHAN DETECTED: File ID \`${fileId}\` generated by User \`${file.userId || 'Unknown'}\`. Commencing localized destruction.`);
          await storage.deleteFile(BUCKET_ID, fileId);
          totalDeleted++;
          console.log(`    [✓] Terminated File ${fileId} successfully.`);
        }
      }

      if (files.length < 100) {
        hasMore = false;
      } else {
        cursor = files[files.length - 1].$id;
      }
    }
    
    console.log(`\n\n[===== GARBAGE COLLECTION SECURED =====]`);
    console.log(`Total Stale Cloud Artifacts Scrutinized: ${totalScanned}`);
    console.log(`Total Orphan Dependencies Terminated: ${totalDeleted}`);
    console.log(`[+] Bucket ${BUCKET_ID} health optimized.`);

  } catch (error) {
    console.error(`\n[!] FATAL CRASH DURING SWEEP PROCESS:`, error.message);
    process.exit(1);
  }
}

main();
